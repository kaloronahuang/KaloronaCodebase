#include <iostream>
using namespace std;

int main()
{
    int num;
    cin >> num;
    if (num == 25)
        cout << "Christmas";
    else if (num == 24)
        cout << "Christmas Eve";
    else if (num == 23)
        cout << "Christmas Eve Eve";
    else if (num == 22)
        cout << "Christmas Eve Eve Eve";
    return 0;
}